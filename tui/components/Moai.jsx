const W = Math.max(...RAW.map((l) => l.length));
const ART = RAW.map((l) => l.padEnd(W));

export const COLS = W;
export const ROWS = ART.length;

export default function Moai({ state = 'empty' }) {
  // 黒曜石が入っていない像は沈む。参照どおり目に黒曜石は置かず、状態は右のパネルが持つ。
  const stone =
    state === 'running' ? color.stoneBright : state === 'fitted' ? color.stone : color.stoneDim;

  // flexShrink={0} は必須。右のパネルが幅を食うと Yoga がこの Box を縮め、
  // 点字行が折り返して絵が2倍の高さに崩れる。
  return (
    <Box flexDirection="column" width={W} flexShrink={0}>
      {ART.map((line, i) => (
        <Text key={i} color={stone}>
          {line}
        </Text>
      ))}
    </Box>
  );
}
